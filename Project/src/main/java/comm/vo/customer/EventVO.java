package user.vo.customer;

public class EventVO {
    private String id, brand, brand_icon, coupon_no, coupon_name, sale_per, grade_no, event_time, is_read;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getBrand_icon() {
        return brand_icon;
    }

    public void setBrand_icon(String brand_icon) {
        this.brand_icon = brand_icon;
    }

    public String getCoupon_no() {
        return coupon_no;
    }

    public void setCoupon_no(String coupon_no) {
        this.coupon_no = coupon_no;
    }

    public String getCoupon_name() {
        return coupon_name;
    }

    public void setCoupon_name(String coupon_name) {
        this.coupon_name = coupon_name;
    }

    public String getSale_per() {
        return sale_per;
    }

    public void setSale_per(String sale_per) {
        this.sale_per = sale_per;
    }

    public String getGrade_no() {
        return grade_no;
    }

    public void setGrade_no(String grade_no) {
        this.grade_no = grade_no;
    }

    public String getEvent_time() {
        return event_time;
    }

    public void setEvent_time(String event_time) {
        this.event_time = event_time;
    }

    public String getIs_read() {
        return is_read;
    }

    public void setIs_read(String is_read) {
        this.is_read = is_read;
    }
}
